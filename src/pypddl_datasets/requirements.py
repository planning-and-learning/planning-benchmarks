"""PDDL requirement metadata: what each domain/task declares, and filtering.

All queries here are metadata-only (requirements.json ships in the wheel,
generated by scripts/extract_requirements.py) — nothing is downloaded.
"""

from __future__ import annotations

import functools
import json
from enum import Enum
from importlib import resources
from typing import Iterable

from .suites import SUITES


class Requirement(str, Enum):
    STRIPS = ":strips"
    TYPING = ":typing"
    NEGATIVE_PRECONDITIONS = ":negative-preconditions"
    DISJUNCTIVE_PRECONDITIONS = ":disjunctive-preconditions"
    EQUALITY = ":equality"
    EXISTENTIAL_PRECONDITIONS = ":existential-preconditions"
    UNIVERSAL_PRECONDITIONS = ":universal-preconditions"
    QUANTIFIED_PRECONDITIONS = ":quantified-preconditions"
    CONDITIONAL_EFFECTS = ":conditional-effects"
    FLUENTS = ":fluents"
    NUMERIC_FLUENTS = ":numeric-fluents"
    OBJECT_FLUENTS = ":object-fluents"
    ADL = ":adl"
    DURATIVE_ACTIONS = ":durative-actions"
    DURATION_INEQUALITIES = ":duration-inequalities"
    CONTINUOUS_EFFECTS = ":continuous-effects"
    DERIVED_PREDICATES = ":derived-predicates"
    TIMED_INITIAL_LITERALS = ":timed-initial-literals"
    PREFERENCES = ":preferences"
    CONSTRAINTS = ":constraints"
    ACTION_COSTS = ":action-costs"


# Composite requirements are replaced by the atomic ones they imply (PDDL spec),
# so ":adl" matches a planner that lists the individual capabilities and vice versa.
_COMPOSITES: dict[Requirement, frozenset[Requirement]] = {
    Requirement.QUANTIFIED_PRECONDITIONS: frozenset(
        {Requirement.EXISTENTIAL_PRECONDITIONS, Requirement.UNIVERSAL_PRECONDITIONS}
    ),
    Requirement.FLUENTS: frozenset({Requirement.NUMERIC_FLUENTS, Requirement.OBJECT_FLUENTS}),
    Requirement.ADL: frozenset(
        {
            Requirement.STRIPS,
            Requirement.TYPING,
            Requirement.NEGATIVE_PRECONDITIONS,
            Requirement.DISJUNCTIVE_PRECONDITIONS,
            Requirement.EQUALITY,
            Requirement.EXISTENTIAL_PRECONDITIONS,
            Requirement.UNIVERSAL_PRECONDITIONS,
            Requirement.CONDITIONAL_EFFECTS,
        }
    ),
    Requirement.TIMED_INITIAL_LITERALS: frozenset(
        {Requirement.TIMED_INITIAL_LITERALS, Requirement.DURATIVE_ACTIONS}
    ),
}

Requirements = Iterable["Requirement | str"]


def _expand(requirements: Requirements) -> frozenset[Requirement]:
    expanded: set[Requirement] = set()
    for value in requirements:
        requirement = Requirement(value)
        expanded.update(_COMPOSITES.get(requirement, frozenset({requirement})))
    return frozenset(expanded)


def _matches(declared: Requirements, supported: Requirements | None, requires: Requirements | None) -> bool:
    expanded = _expand(declared)
    if supported is not None and not expanded <= _expand(supported):
        return False
    if requires is not None and not _expand(requires) <= expanded:
        return False
    return True


@functools.cache
def _metadata() -> dict:
    with resources.as_file(resources.files(__package__) / "requirements.json") as path:
        return json.loads(path.read_text(encoding="utf-8"))


def _entry(name: str) -> dict:
    try:
        return _metadata()[name]
    except KeyError:
        raise KeyError(f"unknown domain {name!r}; see list_domains()") from None


def _suite_scope(suite: str) -> dict[str, set[str] | None]:
    """Domain name -> restricted task file names, or None for all tasks."""
    if suite not in SUITES:
        raise KeyError(f"unknown suite {suite!r}; see list_suites()")
    scope: dict[str, set[str] | None] = {}
    for entry in SUITES[suite]:
        name, _, instance = entry.partition(":")
        if instance:
            problems = scope.setdefault(name, set())
            if problems is not None:
                problems.add(instance)
        else:
            scope[name] = None
    return scope


def domain_requirements(name: str) -> frozenset[Requirement]:
    """The union of the requirements declared by a domain's files."""
    return frozenset(Requirement(value) for value in _entry(name)["requirements"])


def find_domains(
    supported: Requirements | None = None,
    requires: Requirements | None = None,
    suite: str | None = None,
) -> list[str]:
    """Domain names whose declared requirements stay within `supported` and
    include `requires`; optionally restricted to a named suite."""
    names = _suite_scope(suite) if suite is not None else _metadata()
    return sorted(name for name in names if _matches(_entry(name)["requirements"], supported, requires))


def find_tasks(
    supported: Requirements | None = None,
    requires: Requirements | None = None,
    suite: str | None = None,
) -> list[str]:
    """Task names (as accepted by fetch_task) matching the filters."""
    scope = _suite_scope(suite) if suite is not None else None
    names: list[str] = []
    for name in scope if scope is not None else _metadata():
        entry = _entry(name)
        allowed = scope[name] if scope is not None else None
        overrides = entry.get("tasks", {})
        for problem in entry["problems"]:
            if allowed is not None and problem not in allowed and problem.rsplit("/", 1)[-1] not in allowed:
                continue
            if _matches(overrides.get(problem, entry["requirements"]), supported, requires):
                names.append(f"{name}/{problem}")
    return sorted(names)


def find_suites(
    supported: Requirements | None = None,
    requires: Requirements | None = None,
) -> list[str]:
    """Suites in which every domain matches the filters."""
    return sorted(
        suite
        for suite in SUITES
        if all(
            _matches(_entry(entry.partition(":")[0])["requirements"], supported, requires)
            for entry in SUITES[suite]
        )
    )
