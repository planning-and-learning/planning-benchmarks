"""PDDL requirement metadata and filtering over explicit requirements.

The benchmark data is strict-clean: every file declares exactly the atomic
requirements it uses (no aggregates like :adl, :strips always declared), so
the metadata simply mirrors the declarations.

All queries are metadata-only: the requirements.{tasks,domains,suites}.json
files ship in the wheel, generated by pypddl_datasets.scripts.extract_requirements —
nothing is downloaded.
"""

from __future__ import annotations

import functools
import json
from enum import Enum
from importlib import resources
from typing import Iterable, cast

from .suites import SUITES


class Requirement(str, Enum):
    STRIPS = ":strips"
    TYPING = ":typing"
    NEGATIVE_PRECONDITIONS = ":negative-preconditions"
    DISJUNCTIVE_PRECONDITIONS = ":disjunctive-preconditions"
    EQUALITY = ":equality"
    EXISTENTIAL_PRECONDITIONS = ":existential-preconditions"
    UNIVERSAL_PRECONDITIONS = ":universal-preconditions"
    CONDITIONAL_EFFECTS = ":conditional-effects"
    NUMERIC_FLUENTS = ":numeric-fluents"
    OBJECT_FLUENTS = ":object-fluents"
    DURATIVE_ACTIONS = ":durative-actions"
    DURATION_INEQUALITIES = ":duration-inequalities"
    CONTINUOUS_EFFECTS = ":continuous-effects"
    DERIVED_PREDICATES = ":derived-predicates"
    TIMED_INITIAL_LITERALS = ":timed-initial-literals"
    PREFERENCES = ":preferences"
    CONSTRAINTS = ":constraints"
    ACTION_COSTS = ":action-costs"


class CompositeRequirements(str, Enum):
    """Aggregate requirements from the PDDL spec. They are resolved to the
    explicit Requirements they imply at extraction time and never appear in
    the metadata — filter with the explicit ones instead."""

    ADL = ":adl"
    QUANTIFIED_PRECONDITIONS = ":quantified-preconditions"
    FLUENTS = ":fluents"


COMPOSITE_TO_REQUIREMENTS: dict[CompositeRequirements, frozenset[Requirement]] = {
    CompositeRequirements.ADL: frozenset(
        {
            Requirement.STRIPS,
            Requirement.TYPING,
            Requirement.NEGATIVE_PRECONDITIONS,
            Requirement.DISJUNCTIVE_PRECONDITIONS,
            Requirement.EQUALITY,
            Requirement.EXISTENTIAL_PRECONDITIONS,
            Requirement.UNIVERSAL_PRECONDITIONS,
            Requirement.CONDITIONAL_EFFECTS,
        }
    ),
    CompositeRequirements.QUANTIFIED_PRECONDITIONS: frozenset(
        {Requirement.EXISTENTIAL_PRECONDITIONS, Requirement.UNIVERSAL_PRECONDITIONS}
    ),
    CompositeRequirements.FLUENTS: frozenset(
        {Requirement.NUMERIC_FLUENTS, Requirement.OBJECT_FLUENTS}
    ),
}


Requirements = Iterable["Requirement | str"]


def _coerce(requirements: Requirements) -> frozenset[Requirement]:
    return frozenset(Requirement(value) for value in requirements)


def matches(declared: Requirements, supported: Requirements | None, requires: Requirements | None) -> bool:
    requirements = _coerce(declared)
    if supported is not None and not requirements <= _coerce(supported):
        return False
    if requires is not None and not _coerce(requires) <= requirements:
        return False
    return True


@functools.cache
def _load(filename: str) -> object:
    with resources.as_file(resources.files(__package__) / filename) as path:
        return json.loads(path.read_text(encoding="utf-8"))


def _task_metadata() -> dict[str, dict[str, list[str]]]:
    return cast("dict[str, dict[str, list[str]]]", _load("requirements.tasks.json"))


def _domain_metadata() -> dict[str, list[str]]:
    return cast("dict[str, list[str]]", _load("requirements.domains.json"))


def _suite_metadata() -> dict[str, list[str]]:
    return cast("dict[str, list[str]]", _load("requirements.suites.json"))


def _domain_entry(name: str) -> list[str]:
    try:
        return _domain_metadata()[name]
    except KeyError:
        raise KeyError(f"unknown domain {name!r}; see list_domains()") from None


def _suite_scope(suite: str) -> dict[str, set[str] | None]:
    """Domain name -> restricted task file names, or None for all tasks."""
    if suite not in SUITES:
        raise KeyError(f"unknown suite {suite!r}; see list_suites()")
    scope: dict[str, set[str] | None] = {}
    for entry in SUITES[suite]:
        name, _, instance = entry.partition(":")
        if instance:
            problems = scope.setdefault(name, set())
            if problems is not None:
                problems.add(instance)
        else:
            scope[name] = None
    return scope


def domain_requirements(name: str) -> frozenset[Requirement]:
    """The union of the requirements declared by a domain's files."""
    return frozenset(Requirement(value) for value in _domain_entry(name))


def task_requirements(name: str) -> frozenset[Requirement]:
    """The requirements of one task: its own declarations plus its specific
    domain file's, e.g. task_requirements("classical/tests/gripper/test-1.pddl")."""
    tasks = _task_metadata()
    parts = name.split("/")
    for index in range(1, len(parts)):
        domain, problem = "/".join(parts[:index]), "/".join(parts[index:])
        if domain not in tasks:
            continue
        by_problem = tasks[domain]
        if problem not in by_problem:
            matches = [p for p in by_problem if p.rsplit("/", 1)[-1] == problem]
            if len(matches) != 1:
                raise KeyError(f"no task {problem!r} in domain {domain!r}")
            problem = matches[0]
        return frozenset(Requirement(value) for value in by_problem[problem])
    raise KeyError(f"no domain found in task name {name!r}; see list_domains()")


def find_domains(
    supported: Requirements | None = None,
    requires: Requirements | None = None,
    suite: str | None = None,
) -> list[str]:
    """Domain names whose declared requirements stay within `supported` and
    include `requires`; optionally restricted to a named suite."""
    names = _suite_scope(suite) if suite is not None else _domain_metadata()
    return sorted(name for name in names if matches(_domain_entry(name), supported, requires))


def find_tasks(
    supported: Requirements | None = None,
    requires: Requirements | None = None,
    suite: str | None = None,
) -> list[str]:
    """Task names (as accepted by fetch_task) matching the filters, using
    each task's own requirements (tasks within a domain may differ)."""
    tasks = _task_metadata()
    scope = _suite_scope(suite) if suite is not None else None
    names: list[str] = []
    for name in scope if scope is not None else tasks:
        allowed = scope[name] if scope is not None else None
        for problem, declared in tasks[name].items():
            if allowed is not None and problem not in allowed and problem.rsplit("/", 1)[-1] not in allowed:
                continue
            if matches(declared, supported, requires):
                names.append(f"{name}/{problem}")
    return sorted(names)


def find_suites(
    supported: Requirements | None = None,
    requires: Requirements | None = None,
) -> list[str]:
    """Suites matched against the union of their domains' requirements, with
    the same semantics as everywhere else: `supported` keeps suites whose
    every file the caller can handle; `requires` keeps suites in which each
    given requirement is used somewhere."""
    suites = _suite_metadata()
    return sorted(suite for suite, union in suites.items() if matches(union, supported, requires))
