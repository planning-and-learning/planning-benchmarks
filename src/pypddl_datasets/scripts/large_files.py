#!/usr/bin/env python3
"""The repository's large-file system: PDDL files at or above the threshold
are committed as byte-reproducible <name>.pddl.gz twins while the plain files
stay materialized locally and gitignored (a managed block in the repo-root
.gitignore). `pack` creates/refreshes the twins and the gitignore block;
`unpack` materializes the plain files after a fresh clone.
pypddl_datasets.validation.layout enforces that no oversized plain file can
land in git unpacked."""

from __future__ import annotations

import argparse
import gzip
from pathlib import Path

THRESHOLD_BYTES = 50 * 1024 * 1024  # GitHub warns at 50 MiB and blocks at 100 MiB

GITIGNORE_BEGIN = "# >>> large pddl files (generated by pypddl_datasets.scripts.large_files) >>>"
GITIGNORE_END = "# <<< large pddl files <<<"


def packed_files(root: Path) -> list[Path]:
    """The .pddl.gz twins under root."""
    return sorted(path for path in root.rglob("*.pddl.gz") if path.is_file())


def write_reproducible_gz(source: Path, target: Path) -> None:
    """Fixed gzip metadata: repacking identical content yields identical bytes."""
    with open(target, "wb") as out:
        with gzip.GzipFile(filename="", fileobj=out, mode="wb", compresslevel=9, mtime=0) as stream:
            stream.write(source.read_bytes())


def rewrite_gitignore_block(root: Path) -> None:
    """Regenerate the managed block listing every packed plain file."""
    gitignore = root.parent / ".gitignore"
    lines = gitignore.read_text(encoding="utf-8").splitlines() if gitignore.is_file() else []
    if GITIGNORE_BEGIN in lines and GITIGNORE_END in lines:
        del lines[lines.index(GITIGNORE_BEGIN) : lines.index(GITIGNORE_END) + 1]
    while lines and not lines[-1]:
        lines.pop()
    entries = [str(path.relative_to(root.parent).as_posix())[: -len(".gz")] for path in packed_files(root)]
    if entries:
        lines += ["", GITIGNORE_BEGIN, *entries, GITIGNORE_END]
    gitignore.write_text("\n".join(lines) + "\n", encoding="utf-8")


def pack(root: Path, threshold: int) -> int:
    packed = 0
    for path in sorted(root.rglob("*.pddl")):
        if not path.is_file() or path.stat().st_size < threshold:
            continue
        twin = path.with_name(path.name + ".gz")
        if twin.is_file() and gzip.decompress(twin.read_bytes()) == path.read_bytes():
            continue
        write_reproducible_gz(path, twin)
        print(f"  packed {path} ({path.stat().st_size >> 20} MiB -> {twin.stat().st_size >> 20} MiB)")
        packed += 1
    rewrite_gitignore_block(root)
    print(f"[large-files] {packed} files packed, {len(packed_files(root))} twins total; .gitignore block updated")
    return 0


def unpack(root: Path) -> int:
    unpacked = 0
    for twin in packed_files(root):
        plain = twin.with_name(twin.name[: -len(".gz")])
        content = gzip.decompress(twin.read_bytes())
        if plain.is_file() and plain.read_bytes() == content:
            continue
        plain.write_bytes(content)
        print(f"  unpacked {plain}")
        unpacked += 1
    print(f"[large-files] {unpacked} files unpacked, {len(packed_files(root))} twins total")
    return 0


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("command", choices=["pack", "unpack"])
    parser.add_argument("--root", type=Path, default=Path.cwd() / "data")
    parser.add_argument("--threshold-mb", type=int, default=THRESHOLD_BYTES >> 20)
    args = parser.parse_args(argv)
    root: Path = args.root.resolve()
    if not root.is_dir():
        parser.error(f"root is not a directory: {root}")
    if args.command == "pack":
        return pack(root, args.threshold_mb << 20)
    return unpack(root)


if __name__ == "__main__":
    raise SystemExit(main())
